create table EVALUATION_GRADE
(
    EVALUATION_ID      BIGINT  not null,
    EVALUATION_ITEM_ID BIGINT  not null,
    GRADE              INTEGER not null,
    constraint EVALUATION_GRADE_EVALUATION_ID_FK
        foreign key (EVALUATION_ID) references EVALUATION (ID)
            on update cascade,
    constraint EVALUATION_GRADE_EVALUATION_ITEM_ID_FK
        foreign key (EVALUATION_ITEM_ID) references EVALUATION_ITEM (ID)
            on update cascade
);

